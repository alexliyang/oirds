    "class GlobalMercator(object):\n",
    "    \"\"\"\n",
    "    TMS Global Mercator Profile\n",
    "    ---------------------------\n",
    "\n",
    "    Functions necessary for generation of tiles in Spherical Mercator projection,\n",
    "    EPSG:900913 (EPSG:gOOglE, Google Maps Global Mercator), EPSG:3785, OSGEO:41001.\n",
    "\n",
    "    Such tiles are compatible with Google Maps, Microsoft Virtual Earth, Yahoo Maps,\n",
    "    UK Ordnance Survey OpenSpace API, ...\n",
    "    and you can overlay them on top of base maps of those web mapping applications.\n",
    "    \n",
    "    Pixel and tile coordinates are in TMS notation (origin [0,0] in bottom-left).\n",
    "\n",
    "    What coordinate conversions do we need for TMS Global Mercator tiles::\n",
    "\n",
    "         LatLon      <->       Meters      <->     Pixels    <->       Tile    \n",
    "\n",
    "     WGS84 coordinates   Spherical Mercator  Pixels in pyramid  Tiles in pyramid\n",
    "         lat/lon            XY in metres     XY pixels Z zoom      XYZ from TMS\n",
    "        EPSG:4326           EPSG:900913                                        \n",
    "         .----.              ---------               --                TMS      \n",
    "        /      \\     <->     |       |     <->     /----/    <->      Google    \n",
    "        \\      /             |       |           /--------/          QuadTree  \n",
    "         -----               ---------         /------------/                  \n",
    "       KML, public         WebMapService         Web Clients      TileMapService\n",
    "\n",
    "    What is the coordinate extent of Earth in EPSG:900913?\n",
    "\n",
    "      [-20037508.342789244, -20037508.342789244, 20037508.342789244, 20037508.342789244]\n",
    "      Constant 20037508.342789244 comes from the circumference of the Earth in meters,\n",
    "      which is 40 thousand kilometers, the coordinate origin is in the middle of extent.\n",
    "      In fact you can calculate the constant as: 2 * math.pi * 6378137 / 2.0\n",
    "      $ echo 180 85 | gdaltransform -s_srs EPSG:4326 -t_srs EPSG:900913\n",
    "      Polar areas with abs(latitude) bigger then 85.05112878 are clipped off.\n",
    "\n",
    "    What are zoom level constants (pixels/meter) for pyramid with EPSG:900913?\n",
    "\n",
    "      whole region is on top of pyramid (zoom=0) covered by 256x256 pixels tile,\n",
    "      every lower zoom level resolution is always divided by two\n",
    "      initialResolution = 20037508.342789244 * 2 / 256 = 156543.03392804062\n",
    "\n",
    "    What is the difference between TMS and Google Maps/QuadTree tile name convention?\n",
    "\n",
    "      The tile raster itself is the same (equal extent, projection, pixel size),\n",
    "      there is just different identification of the same raster tile.\n",
    "      Tiles in TMS are counted from [0,0] in the bottom-left corner, id is XYZ.\n",
    "      Google placed the origin [0,0] to the top-left corner, reference is XYZ.\n",
    "      Microsoft is referencing tiles by a QuadTree name, defined on the website:\n",
    "      http://msdn2.microsoft.com/en-us/library/bb259689.aspx\n",
    "\n",
    "    The lat/lon coordinates are using WGS84 datum, yeh?\n",
    "\n",
    "      Yes, all lat/lon we are mentioning should use WGS84 Geodetic Datum.\n",
    "      Well, the web clients like Google Maps are projecting those coordinates by\n",
    "      Spherical Mercator, so in fact lat/lon coordinates on sphere are treated as if\n",
    "      the were on the WGS84 ellipsoid.\n",
    "    \n",
    "      From MSDN documentation:\n",
    "      To simplify the calculations, we use the spherical form of projection, not\n",
    "      the ellipsoidal form. Since the projection is used only for map display,\n",
    "      and not for displaying numeric coordinates, we don't need the extra precision\n",
    "      of an ellipsoidal projection. The spherical projection causes approximately\n",
    "      0.33 percent scale distortion in the Y direction, which is not visually noticable.\n",
    "\n",
    "    How do I create a raster in EPSG:900913 and convert coordinates with PROJ.4?\n",
    "\n",
    "      You can use standard GIS tools like gdalwarp, cs2cs or gdaltransform.\n",
    "      All of the tools supports -t_srs 'epsg:900913'.\n",
    "\n",
    "      For other GIS programs check the exact definition of the projection:\n",
    "      More info at http://spatialreference.org/ref/user/google-projection/\n",
    "      The same projection is degined as EPSG:3785. WKT definition is in the official\n",
    "      EPSG database.\n",
    "\n",
    "      Proj4 Text:\n",
    "        +proj=merc +a=6378137 +b=6378137 +lat_ts=0.0 +lon_0=0.0 +x_0=0.0 +y_0=0\n",
    "        +k=1.0 +units=m +nadgrids=@null +no_defs\n",
    "\n",
    "      Human readable WKT format of EPGS:900913:\n",
    "         PROJCS[\"Google Maps Global Mercator\",\n",
    "             GEOGCS[\"WGS 84\",\n",
    "                 DATUM[\"WGS_1984\",\n",
    "                     SPHEROID[\"WGS 84\",6378137,298.2572235630016,\n",
    "                         AUTHORITY[\"EPSG\",\"7030\"]],\n",
    "                     AUTHORITY[\"EPSG\",\"6326\"]],\n",
    "                 PRIMEM[\"Greenwich\",0],\n",
    "                 UNIT[\"degree\",0.0174532925199433],\n",
    "                 AUTHORITY[\"EPSG\",\"4326\"]],\n",
    "             PROJECTION[\"Mercator_1SP\"],\n",
    "             PARAMETER[\"central_meridian\",0],\n",
    "             PARAMETER[\"scale_factor\",1],\n",
    "             PARAMETER[\"false_easting\",0],\n",
    "             PARAMETER[\"false_northing\",0],\n",
