    "    def __init__(self, tileSize=256):\n",
    "        \"Initialize the TMS Global Mercator pyramid\"\n",
    "        self.tileSize = tileSize\n",
    "        self.initialResolution = 2 * math.pi * 6378137 / self.tileSize\n",
    "        # 156543.03392804062 for tileSize 256 pixels\n",
    "        self.originShift = 2 * math.pi * 6378137 / 2.0\n",
    "        # 20037508.342789244\n",
    "\n",
    "    def LatLonToMeters(self, lat, lon ):\n",
    "        \"Converts given lat/lon in WGS84 Datum to XY in Spherical Mercator EPSG:900913\"\n",
    "\n",
    "        mx = lon * self.originShift / 180.0\n",
    "        my = math.log( math.tan((90 + lat) * math.pi / 360.0 )) / (math.pi / 180.0)\n",
    "\n",
    "        my = my * self.originShift / 180.0\n",
    "        return mx, my\n",
    "\n",
    "    def MetersToLatLon(self, mx, my ):\n",
    "        \"Converts XY point from Spherical Mercator EPSG:900913 to lat/lon in WGS84 Datum\"\n",
    "\n",
    "        lon = (mx / self.originShift) * 180.0\n",
    "        lat = (my / self.originShift) * 180.0\n",
    "\n",
    "        lat = 180 / math.pi * (2 * math.atan( math.exp( lat * math.pi / 180.0)) - math.pi / 2.0)\n",
    "        return lat, lon\n",
    "\n",
    "    def PixelsToMeters(self, px, py, zoom):\n",
    "        \"Converts pixel coordinates in given zoom level of pyramid to EPSG:900913\"\n",
    "\n",
    "        res = self.Resolution( zoom )\n",
    "        mx = px * res - self.originShift\n",
    "        my = py * res - self.originShift\n",
    "        return mx, my\n",
    "        \n",
    "    def MetersToPixels(self, mx, my, zoom):\n",
    "        \"Converts EPSG:900913 to pyramid pixel coordinates in given zoom level\"\n",
    "                \n",
    "        res = self.Resolution( zoom )\n",
    "        px = (mx + self.originShift) / res\n",
    "        py = (my + self.originShift) / res\n",
    "        return px, py\n",
    "    \n",
    "    def PixelsToTile(self, px, py):\n",
    "        \"Returns a tile covering region in given pixel coordinates\"\n",
    "\n",
    "        tx = int( math.ceil( px / float(self.tileSize) ) - 1 )\n",
    "        ty = int( math.ceil( py / float(self.tileSize) ) - 1 )\n",
    "        return tx, ty\n",
    "\n",
    "    def PixelsToRaster(self, px, py, zoom):\n",
    "        \"Move the origin of pixel coordinates to top-left corner\"\n",
    "        \n",
    "        mapSize = self.tileSize << zoom\n",
    "        return px, mapSize - py\n",
    "        \n",
    "    def MetersToTile(self, mx, my, zoom):\n",
    "        \"Returns tile for given mercator coordinates\"\n",
    "        \n",
    "        px, py = self.MetersToPixels( mx, my, zoom)\n",
    "        return self.PixelsToTile( px, py)\n",
    "\n",
    "    def TileBounds(self, tx, ty, zoom):\n",
    "        \"Returns bounds of the given tile in EPSG:900913 coordinates\"\n",
    "        \n",
    "        minx, miny = self.PixelsToMeters( tx*self.tileSize, ty*self.tileSize, zoom )\n",
    "        maxx, maxy = self.PixelsToMeters( (tx+1)*self.tileSize, (ty+1)*self.tileSize, zoom )\n",
    "        return ( minx, miny, maxx, maxy )\n",
    "\n",
    "    def TileLatLonBounds(self, tx, ty, zoom ):\n",
    "        \"Returns bounds of the given tile in latutude/longitude using WGS84 datum\"\n",
    "\n",
    "        bounds = self.TileBounds( tx, ty, zoom)\n",
    "        minLat, minLon = self.MetersToLatLon(bounds[0], bounds[1])\n",
    "        maxLat, maxLon = self.MetersToLatLon(bounds[2], bounds[3])\n",
    "        \n",
    "        return ( minLat, minLon, maxLat, maxLon )\n",
    "        \n",
    "    def Resolution(self, zoom ):\n",
    "        \"Resolution (meters/pixel) for given zoom level (measured at Equator)\"\n",
    "        \n",
    "        # return (2 * math.pi * 6378137) / (self.tileSize * 2**zoom)\n",
    "        return self.initialResolution / (2**zoom)\n",
    "        \n",
    "    def ZoomForPixelSize(self, pixelSize ):\n",
    "        \"Maximal scaledown zoom of the pyramid closest to the pixelSize.\"\n",
    "        \n",
    "        for i in range(30):\n",
    "            if pixelSize > self.Resolution(i):\n",
    "                return i-1 if i!=0 else 0 # We don't want to scale up\n",
    "\n",
    "    def GoogleTile(self, tx, ty, zoom):\n",
    "        \"Converts TMS tile coordinates to Google Tile coordinates\"\n",
    "        \n",
    "        # coordinate origin is moved from bottom-left to top-left corner of the extent\n",
    "        return tx, (2**zoom - 1) - ty\n",
    "\n",
    "    def QuadTree(self, tx, ty, zoom ):\n",
    "        \"Converts TMS tile coordinates to Microsoft QuadTree\"\n",
    "        \n",
    "        quadKey = \"\"\n",
    "        ty = (2**zoom - 1) - ty\n",
    "        for i in range(zoom, 0, -1):\n",
    "            digit = 0\n",
    "            mask = 1 << (i-1)\n",
    "            if (tx & mask) != 0:\n",
    "                digit += 1\n",
    "            if (ty & mask) != 0:\n",
    "                digit += 2\n",
    "            quadKey += str(digit)\n",
    "            \n",
    "        return quadKey\n",
    "\n",
    "#---------------------\n",
    "\n",
    "class GlobalGeodetic(object):\n",
    "    \"\"\"\n",
    "    TMS Global Geodetic Profile\n",
    "    ---------------------------\n",
    "\n",
    "    Functions necessary for generation of global tiles in Plate Carre projection,\n",
    "    EPSG:4326, \"unprojected profile\".\n",
    "\n",
    "    Such tiles are compatible with Google Earth (as any other EPSG:4326 rasters)\n",
    "    and you can overlay the tiles on top of OpenLayers base map.\n",
    "    \n",
    "    Pixel and tile coordinates are in TMS notation (origin [0,0] in bottom-left).\n",
    "\n",
    "    What coordinate conversions do we need for TMS Global Geodetic tiles?\n",
    "\n",
    "      Global Geodetic tiles are using geodetic coordinates (latitude,longitude)\n",
    "      directly as planar coordinates XY (it is also called Unprojected or Plate\n",
    "      Carre). We need only scaling to pixel pyramid and cutting to tiles.\n",
    "      Pyramid has on top level two tiles, so it is not square but rectangle.\n",
    "      Area [-180,-90,180,90] is scaled to 512x256 pixels.\n",
    "      TMS has coordinate origin (for pixels and tiles) in bottom-left corner.\n",
    "      Rasters are in EPSG:4326 and therefore are compatible with Google Earth.\n",
    "\n",
    "         LatLon      <->      Pixels      <->     Tiles    \n",
    "\n",
    "     WGS84 coordinates   Pixels in pyramid  Tiles in pyramid\n",
    "         lat/lon         XY pixels Z zoom      XYZ from TMS\n",
    "        EPSG:4326                                          \n",
    "         .----.                ----                        \n",
    "        /      \\     <->    /--------/    <->      TMS      \n",
    "        \\      /         /--------------/                  \n",
    "         -----        /--------------------/                \n",
    "       WMS, KML    Web Clients, Google Earth  TileMapService\n",
    "    \"\"\"\n",
    "    tileSize = 256\n",
    "\n",
    "    def __init__(self, tileSize = 256):\n",
    "        self.tileSize = tileSize\n",
    "\n",
    "    def LatLonToPixels(self, lat, lon, zoom):\n",
    "        \"Converts lat/lon to pixel coordinates in given zoom of the EPSG:4326 pyramid\"\n",
    "        print self.tileSize\n",
    "        res = 180.0 / self.tileSize / 2**zoom\n",
    "        px = (180 + lat) / res\n",
    "        py = (90 + lon) / res\n",
    "        return px, py\n",
    "\n",
    "    def PixelsToTile(self, px, py):\n",
    "        \"Returns coordinates of the tile covering region in pixel coordinates\"\n",
    "\n",
    "        tx = int( math.ceil( px / float(self.tileSize) ) - 1 )\n",
    "        ty = int( math.ceil( py / float(self.tileSize) ) - 1 )\n",
    "        return tx, ty\n",
    "\n",
    "    def Resolution(self, zoom ):\n",
    "        \"Resolution (arc/pixel) for given zoom level (measured at Equator)\"\n",
    "        \n",
    "        return 180 / 256.0 / 2**zoom\n",
    "        #return 180 / float( 1 << (8+zoom) )\n",
    "\n",
    "    def TileBounds(self, tx, ty, zoom):\n",
    "        \"Returns bounds of the given tile\"\n",
    "        res = 180.0 / 256.0 / 2.0**zoom\n",
    "        return (\n",
    "            tx*256.0*res - 180.0,\n",
    "            ty*256.0*res - 90.0,\n",
    "            (tx+1)*256.0*res - 180.0,\n",
    "            (ty+1)*256.0*res - 90.0\n",
    "        )\n",
